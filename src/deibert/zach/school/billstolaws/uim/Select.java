    private final UIManager.LookAndFeelInfo LaFInfos[];
    private final String displayNames[];
    private final String classNames[];

    public Select(final Switch parent) {
        initComponents();
        LaFInfos = UIManager.getInstalledLookAndFeels();
        displayNames = new String[LaFInfos.length];
        classNames = new String[LaFInfos.length];
        int def = -1;
        for ( int i = 0; i < LaFInfos.length; i++ ) {
            displayNames[i] = LaFInfos[i].getName();
            classNames[i] = LaFInfos[i].getClassName();
            if ( displayNames[i].equals("Nimbus") ) {
                def = i;
            }
        }
        jComboBox1.setModel(new DefaultComboBoxModel(displayNames));
        jComboBox1.setSelectedIndex(def);
        jButton1.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent ae) {
                EventQueue.invokeLater(new Runnable() {
                    @Override
                    public void run() {
                        parent.selectClass(classNames[jComboBox1.getSelectedIndex()]);
                    }
                });
            }
        });
    }


